export type DictKey = keyof typeof dict;

interface I18nStore {
  locale: Locale;
  setLocale: (l: Locale) => void;
}

export const useI18n = create<I18nStore>((set) => ({
  locale: loadLocale(),
  setLocale: (l) => {
    try {
      localStorage.setItem(LOCALE_KEY, l);
    } catch {
      // ignore
    }
    set({ locale: l });
  },
}));

// UI locale hook
export function useT() {
  const locale = useI18n((s) => s.locale);
  return (key: DictKey) => dict[key][locale];
}

// Poster locale hook: never renders in Chinese.
// If UI locale is 'zh', poster falls back to English.
export function usePosterLocale(): Exclude<Locale, "zh"> {
  const locale = useI18n((s) => s.locale);
  return locale === "zh" ? "en" : locale;
}

export function useTPoster() {
  const posterLocale = usePosterLocale();
  return (key: DictKey) => dict[key][posterLocale];
}

export function getUsagePresets(locale: Locale): string[] {
  const keys: DictKey[] = [
    "preset_commercial",
    "preset_residential",
    "preset_hotel",
    "preset_hospital",
    "preset_office",
    "preset_transit",
    "preset_industrial",
    "preset_mall",
  ];
  return keys.map((k) => dict[k][locale]);
}
